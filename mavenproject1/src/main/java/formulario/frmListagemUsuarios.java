    public void findAll(){
        DefaultTableModel modelo = (DefaultTableModel) tbListagem.getModel();
        modelo.setNumRows(0);
        UsuariosDAO usu = new UsuariosDAO();
        
        for (Usuarios usuario : usu.findAll()) {
            modelo.addRow(new Object[] {
                usuario.getId(),
                usuario.getNome(),
                usuario.getEmail(),
                usuario.getLogin()
            });
        }
    }
    
    public void findByName(String nome){
        DefaultTableModel modelo = (DefaultTableModel) tbListagem.getModel();
        modelo.setNumRows(0);
        UsuariosDAO usu = new UsuariosDAO();
        
        for (Usuarios usuario : usu.findByName(nome)) {
            modelo.addRow(new Object[] {
                usuario.getId(),
                usuario.getNome(),
                usuario.getEmail(),
                usuario.getLogin()
            });
        }
    }

